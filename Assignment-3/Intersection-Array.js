// Find the max number
function findMaxNumber(myArray) {
    let maxNumber = myArray[0];
    for (i = 0; i < myArray / length; i++) {
        if (myArray[i] > maxNumber) {
            maxNumber = myArray[i];
        }
        return maxNumber;
    };
}
